/**
 * 获取今天的恋爱成语
 * @returns {{ index: number, idiom: string }} 当天的成语和索引
 */
export function getTodayLoveIdiom(): { index: number; idiom: string } {
  const today = new Date()
  const startOfYear = new Date(today.getFullYear(), 0, 1) // 1月1日
  const dayOfYear = Math.floor((today.getTime() - startOfYear.getTime()) / (1000 * 60 * 60 * 24))
  const index = dayOfYear % LOVE_IDIOMS.length
  return {
    index,
    idiom: LOVE_IDIOMS[index]
  }
}
